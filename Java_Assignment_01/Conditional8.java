public class Conditional8 {
	public static void main(String[] args)
	{
		int a=20;
		if(a>10 & a%2==0) 
		{
			System.out.println("number is greater than 10 and it is even number");
		}
		else if(a==2 || a%2!=0) {
			System.out.println("the number is odd");
		}
		else 
		{
			System.out.println("entered number is wrong");
		}
	}
	}

